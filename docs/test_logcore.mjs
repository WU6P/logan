// test_logcore.mjs — Node unit tests for logcore.js (the JS port of logan.py).
//
// No dependencies. Run:  node test_logcore.mjs
//
// Mirrors the intent of the Python suite (test_logan.py): parsing, DXCC
// resolution with the call-area overrides, classify() source priority, the
// analyze() report, and the CBS text report. When the public N6RO fixture and
// a Python interpreter are available it also runs a full byte-for-byte parity
// check against the Python engine; otherwise that test skips.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';
import * as core from './logcore.js';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.dirname(HERE);
const J = (n) => JSON.parse(fs.readFileSync(path.join(HERE, n), 'utf8'));
core.setData({ dxcc: J('dxcc.json'), itu: J('itu.json'), rare: J('rare.json'), solar: J('solar.json') });

let pass = 0, fail = 0, skip = 0;
function ok(cond, msg) { if (cond) { pass++; } else { fail++; console.error('  FAIL:', msg); } }
function eq(a, b, msg) { ok(JSON.stringify(a) === JSON.stringify(b), `${msg}  (got ${JSON.stringify(a)}, want ${JSON.stringify(b)})`); }

// ---- ADIF / Cabrillo parsing ----
const adif = '<CALL:4>W1AW <QSO_DATE:8>20240615 <TIME_ON:6>142530 <BAND:3>20M <MODE:2>CW <EOR>' +
  '<CALL:4>DL1A <QSO_DATE:8>20240615 <TIME_ON:4>1430 <BAND:3>20M <MODE:2>CW <EOR>';
let recs = core.parseAdifRecords(adif);
eq(recs.length, 2, 'ADIF: two records');
eq(recs[0].CALL, 'W1AW', 'ADIF: first call');
eq(recs[1].TIME_ON, '1430', 'ADIF: short time kept');

const withHeader = 'Generated by X\n<PROGRAMID:3>abc<EOH>\n<CALL:4>K1XX <QSO_DATE:8>20240101 <TIME_ON:6>000000 <EOR>';
eq(core.parseAdifRecords(withHeader).length, 1, 'ADIF: header skipped');

const cab = `START-OF-LOG: 3.0
CALLSIGN: N6RO
CONTEST: CQ-WW-CW
OPERATORS: N6RO K3EST
QSO: 14000 CW 2024-11-23 0000 N6RO 599 03 JA1ABC 599 25
QSO: 21000 CW 2024-11-23 0001 N6RO 599 03 DL1XYZ 599 14
END-OF-LOG:`;
let crecs = core.parseCabrilloRecords(cab);
eq(crecs.length, 2, 'Cabrillo: two QSOs');
eq(crecs[0].CALL, 'JA1ABC', 'Cabrillo: their call parsed');
eq(crecs[0].BAND, '20M', 'Cabrillo: freq→band');
eq(crecs[0].STATION_CALLSIGN, 'N6RO', 'Cabrillo: station header copied');
eq(crecs[0].SRX_STRING, '25', 'Cabrillo: received exchange');
eq(core.recordsFromText(cab).length, 2, 'auto-detect: Cabrillo');
eq(core.recordsFromText(adif).length, 2, 'auto-detect: ADIF');

// ---- band / datetime helpers ----
eq(core.khzToBand(14025), '20M', 'khzToBand 20M');
eq(core.khzToBand(7005), '40M', 'khzToBand 40M');
eq(core.khzToBand(99999), '', 'khzToBand out of band');
eq(core.qsoDatetime({ QSO_DATE: '20240615', TIME_ON: '142530' }).getUTCHours(), 14, 'qsoDatetime hour');
eq(core.qsoDatetime({ QSO_DATE: 'bad', TIME_ON: '' }), null, 'qsoDatetime rejects bad date');
eq(core.qsoDatetime({ QSO_DATE: '20241332', TIME_ON: '' }), null, 'qsoDatetime rejects month 13');

// ---- pyround (banker's rounding, matches Python round()) ----
eq(core.pyround(0.5), 0, 'pyround 0.5→0 (even)');
eq(core.pyround(1.5), 2, 'pyround 1.5→2 (even)');
eq(core.pyround(2.5), 2, 'pyround 2.5→2 (even)');
eq(core.pyround(33.35, 1), 33.4, 'pyround 1dp');

// ---- DXCC resolution + call-area overrides ----
const ent = (call) => (core.resolveDxcc(call) || {}).entity;
eq(ent('AH6XY'), 'Hawaii', 'override: AH6→Hawaii (not USA)');
eq(ent('KH6ABC'), 'Hawaii', 'override: KH6→Hawaii');
eq(ent('KH7K'), 'Kure I.', 'exception: KH7K stays Kure');
eq(ent('KP4AB'), 'Puerto Rico', 'override: KP4→Puerto Rico');
eq(ent('UA0FZ'), 'Asiatic Russia', 'override: UA0→Asiatic Russia');
eq(ent('EA8XX'), 'Canary Is.', 'override: EA8→Canary Is.');
eq(ent('W1AW'), 'United States of America', 'plain W1AW→USA');
ok((ent('DL/W1AW') || '').includes('Germany'), 'portable: prefix DL wins (Germany)');
eq(ent('KG4AA'), 'Guantanamo Bay', 'KG4+2 letters → Guantanamo');
eq(ent('KG4AAA'), 'United States of America', 'KG4+3 → USA');

// classify source priority + zone pinning
const cls = (q) => core.classify(typeof q === 'string' ? { CALL: q } : q);
eq(cls('JA1ABC')[0], 'AS', 'classify: JA→Asia');
eq(cls('DL1ABC')[4], 'pdf', 'classify: source pdf');
// ITU fallback for a DXCC catch-all collapse (France TM → ITU series)
{ const c = cls('TM2Y'); ok(c[3] === 'France' || c[0] === 'EU', 'classify: TM resolves to France/EU'); }

// refineDomestic → US call-area centroid
{ const [lat, lon, dom] = core.refineDomestic('W6XX', 'United States of America', 39, -98);
  eq([lat, lon, dom], [37.0, -120.0, true], 'refineDomestic: area 6 centroid'); }

// ---- analyze() report ----
const sample = core.parseCabrilloRecords(cab);
const rep = core.analyze(sample, ['sample']);
eq(rep.meta.total, 2, 'analyze: total');
eq(rep.meta.unique_calls, 2, 'analyze: unique');
eq(rep.meta.n_bands, 2, 'analyze: bands');
ok(rep.bandOrder.includes('20M') && rep.bandOrder.includes('15M'), 'analyze: bandOrder');
ok(rep.continents.some(c => c.code === 'AS'), 'analyze: Asia present');
ok(rep.cbs.includes('Gross QSOs=2'), 'analyze: CBS embedded');
ok(rep.cbs.includes('Cabrillo Statistics'), 'analyze: CBS banner');

// filters
const filt = core.analyze(sample, ['sample'], { bands: ['20M'] });
eq(filt.meta.total, 1, 'analyze: band filter');

// empty
eq(core.analyze([], ['x']).meta.total, 0, 'analyze: empty log');

// ---- full parity vs Python engine (skips if fixture / python missing) ----
const fixture = path.join(ROOT, 'doc', 'n6ro_2024cw.log');
if (fs.existsSync(fixture) && fs.existsSync(path.join(ROOT, 'logan.py'))) {
  try {
    const py = execFileSync('python3', ['-c',
      `import json,logan;t=open('${fixture}').read();q=logan.records_from_text(t);` +
      `r=logan.analyze(q,['n6ro_2024cw.log']);r.pop('id',None);print(json.dumps(r))`],
      { cwd: ROOT, encoding: 'utf8', maxBuffer: 1 << 28 });
    const pyRes = JSON.parse(py);
    const text = fs.readFileSync(fixture, 'utf8');
    const { qsos, sources } = core.analyzeUpload(['NAME:n6ro_2024cw.log\n' + text]);
    const jsRes = core.analyze(qsos, sources);
    delete jsRes.id;
    // normalize float-vs-int (JSON round-trip) then compare
    const norm = (x) => JSON.parse(JSON.stringify(x), (k, v) => (typeof v === 'number' && Number.isInteger(v)) ? v : v);
    eq(jsRes.cbs, pyRes.cbs, 'parity: CBS text identical');
    const jc = JSON.stringify(norm({ ...jsRes, cbs: 0 }));
    const pc = JSON.stringify(norm({ ...pyRes, cbs: 0 }));
    ok(jc === pc, 'parity: full JSON identical (N6RO 6592-QSO log)');
    if (jc !== pc) {
      // help locate the first divergence
      for (const key of Object.keys(pyRes)) {
        if (key === 'cbs') continue;
        if (JSON.stringify(norm(jsRes[key])) !== JSON.stringify(norm(pyRes[key]))) console.error('    diverges at key:', key);
      }
    }
  } catch (e) {
    skip++; console.warn('  SKIP parity (python3 unavailable or errored):', e.message.split('\n')[0]);
  }
} else {
  skip++; console.warn('  SKIP parity (fixture doc/n6ro_2024cw.log or logan.py absent)');
}

console.log(`\n${pass} passed, ${fail} failed, ${skip} skipped`);
process.exit(fail ? 1 : 0);
